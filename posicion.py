class Posicion:
    def __init__(self, fila, columna):
        self.fila = fila
        self.columna = columna

"""la clase Posicion. Recibe dos argumentos, fila y columna, que representan la posición en la cuadrícula.
Dentro del constructor, se asigna el valor de fila al atributo self.fila y el valor de columna al atributo self.columna. 
Esto significa que cada instancia de la clase Posicion tendrá sus propios atributos fila y columna, 
que pueden ser accedidos y modificados posteriormente."""

